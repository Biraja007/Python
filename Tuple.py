#Tuple is a container that holds many objects under a single name.
#Tuple is immutable; means it cannot be modified.
#Syntax: TupleName = (Object1 , Object2 , Object3)
DateOfBirth = ("01-01-2016" , "13-06-1998" , "14-04-1894")
print (DateOfBirth)

#Tuple can be accesed by specifying the index of that value.
DateOfBirth [1]

#Tuple can be deleted using 'del' keyword.
MyNumber = ("123","456","789")
print (MyNumber)
del (MyNumber)
print (MyNumber)
#You will get a error as the tuple is deleted at last.
